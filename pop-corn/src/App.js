const average = (arr) =>
  arr.reduce((acc, cur, i, arr) => acc + cur / arr.length, 0);

const KEY = "86e7b74";

export default function App() {
  const [movies, setMovies] = useState([]);
  const [watched, setWatched] = useState([]);

  // for loading
  const [isLoading, setIsLoading] = useState(false);

  // for handel error
  const [error, setError] = useState("");

  const [selectId, setSelectId] = useState(null);

  const [query, setQuery] = useState("");
  // const tempQuery = "interstellar";

  // best demonstration of useEffect hook

  // useEffect(function(){
  //   console.log("After initial ender");
  // },[]);

  // useEffect(function(){
  //   console.log("After every render");
  // })

  // useEffect(function(){
  //   console.log("D");
  // },[query]);

  // console.log("During render");

  function handleSelectMovie(id) {
    setSelectId((selectId) => (id === selectId ? null : id));
  }
  function handleCloseMovie() {
    setSelectId(null);
  }

  // useEffect does not return anything so it don't store the result in any variable but instead we passing a function that contains the code that we want to run as a side effect
  useEffect(
    function () {
      async function fetchMovies() {
        try {
          setIsLoading(true);
          setError("");
          const res = await fetch(
            `http://www.omdbapi.com/?i=tt3896198&apikey=${KEY}&s=${query}`
          );

          // handling error here
          if (!res.ok) throw new Error("Something went wrong with fetching");

          const data = await res.json();

          // if searched key is not found then this error throw
          if (data.Response === "False") throw new Error("Movies not found");

          setMovies(data.Search);
          // console.log(data.Search);
          // setIsLoading(false);
        } catch (err) {
          console.log(err.message);
          setError(err.message);
        } finally {
          setIsLoading(false);
        }
      }

      if (query.length < 3) {
        setMovies([]);
        setError("");
        return;
      }
      fetchMovies();
    },
    [query]
  );
  // [] empty dependency array runs only single times the component renders

  return (
    <>
      <NavBar>
        <Search query={query} setQuery={setQuery} />
        <NumResults movies={query} />
      </NavBar>

      <Main>
        <Box>
          {/* {isLoading ? <Loader /> : <MovieList movies={movies} />} */}
          {/* if internet is slow then isLoading is true then this components renders */}
          {isLoading && <Loader />}
          {/* if everything is fine then this components renders */}
          {!isLoading && !error && (
            <MovieList movies={movies} onSelectMovie={handleSelectMovie} />
          )}
          {/* if there is an error or not found or internet error then this component will execute */}
          {error && <ErrorMessage message={error} />}
        </Box>

        <Box>
          {selectId ? (
            <MovieDetails
              selectedId={selectId}
              handleCloseMovie={handleCloseMovie}
            />
          ) : (
            <>
              <WatchedSummary watched={watched} />
              <WatchedMoviesList watched={watched} />
            </>
          )}
        </Box>
      </Main>
    </>
  );
}

